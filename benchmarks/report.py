"""Generate markdown benchmark report from results.json."""

import json
import sys
from pathlib import Path


def generate_report(results_path, output_path=None):
    """Read results.json and produce a markdown report."""
    with open(results_path) as f:
        r = json.load(f)

    if output_path is None:
        output_path = str(Path(results_path).parent / "BENCHMARK_REPORT.md")

    lines = []
    w = lines.append

    w("# 🧪 BDH Graph Harness — Benchmark Report\n")
    w(f"**Date:** {r['timestamp']}  ")
    w(f"**Vault:** {r['vault_size']} notes  ")
    w(f"**Dataset:** 15 queries across 5 categories\n")

    # ── Method comparison table ──────────────────────────────────────────
    w("## Method Comparison\n")
    key_metrics = ["precision@5", "recall@5", "f1@5", "ndcg@5", "mrr", "latency_ms"]

    w("| Metric | Hybrid (α=0.7) | Vector-only | BM25-only |")
    w("|--------|---------------|-------------|-----------|")

    for m in key_metrics:
        row = f"| **{m}** |"
        for method in ["hybrid", "vector", "bm25"]:
            agg = r["methods"][method]["aggregate"]
            if m in agg:
                stats = agg[m]
                if m == "latency_ms":
                    row += f" {stats['mean']:.1f}ms ± {stats['std']:.1f} |"
                else:
                    row += f" {stats['mean']:.3f} ± {stats['std']:.3f} |"
            else:
                row += " — |"
        w(row)

    w("")

    # ── Per-category breakdown (hybrid) ─────────────────────────────────
    w("## Per-Category Results (Hybrid)\n")
    w("| Category | Queries | MRR | P@5 | R@5 | F1@5 | NDCG@5 |")
    w("|----------|---------|-----|-----|-----|------|--------|")

    hybrid_queries = r["methods"]["hybrid"]["per_query"]
    categories = {}
    for q in hybrid_queries:
        cat = q["category"]
        if cat not in categories:
            categories[cat] = []
        categories[cat].append(q)

    for cat in sorted(categories.keys()):
        qs = categories[cat]
        n = len(qs)
        mrr = sum(q["mrr"] for q in qs) / n
        p5 = sum(q["precision@5"] for q in qs) / n
        r5 = sum(q["recall@5"] for q in qs) / n
        f5 = sum(q["f1@5"] for q in qs) / n
        ndcg = sum(q["ndcg@5"] for q in qs) / n
        w(f"| {cat} | {n} | {mrr:.3f} | {p5:.3f} | {r5:.3f} | {f5:.3f} | {ndcg:.3f} |")

    w("")

    # ── Per-query detail (hybrid) ───────────────────────────────────────
    w("## Per-Query Detail (Hybrid)\n")
    w("| # | Query | Category | MRR | P@5 | R@5 | Hits | n_act | Latency |")
    w("|---|-------|----------|-----|-----|-----|------|-------|---------|")

    for i, q in enumerate(hybrid_queries, 1):
        query_short = q["query"][:45] + ("…" if len(q["query"]) > 45 else "")
        hit_pos = q["first_hit_position"]
        hit_str = f"#{hit_pos}" if hit_pos > 0 else "miss"
        w(f"| {i} | {query_short} | {q['category']} | {q['mrr']:.2f} | "
          f"{q['precision@5']:.2f} | {q['recall@5']:.2f} | {hit_str} | "
          f"{q['n_activated']} | {q['latency_ms']:.0f}ms |")

    w("")

    # ── Key findings ────────────────────────────────────────────────────
    w("## Key Findings\n")

    hybrid_mrr = r["methods"]["hybrid"]["aggregate"]["mrr"]["mean"]
    vector_mrr = r["methods"]["vector"]["aggregate"]["mrr"]["mean"]
    bm25_mrr = r["methods"]["bm25"]["aggregate"]["mrr"]["mean"]

    hybrid_p5 = r["methods"]["hybrid"]["aggregate"]["precision@5"]["mean"]
    vector_p5 = r["methods"]["vector"]["aggregate"]["precision@5"]["mean"]

    hybrid_lat = r["methods"]["hybrid"]["aggregate"]["latency_ms"]["mean"]
    vector_lat = r["methods"]["vector"]["aggregate"]["latency_ms"]["mean"]

    w(f"1. **Hybrid search outperforms single methods:** MRR {hybrid_mrr:.3f} vs "
      f"vector {vector_mrr:.3f} vs BM25 {bm25_mrr:.3f}")
    w(f"2. **Precision@5:** Hybrid {hybrid_p5:.3f} vs Vector {vector_p5:.3f}")
    w(f"3. **Latency:** Hybrid {hybrid_lat:.1f}ms vs Vector {vector_lat:.1f}ms "
      f"(overhead: {((hybrid_lat/vector_lat - 1)*100):+.1f}%)")

    # Count misses
    misses = sum(1 for q in hybrid_queries if q["first_hit_position"] < 0)
    w(f"4. **Misses:** {misses}/{len(hybrid_queries)} queries have no relevant note in activated set")

    w("")
    w("---\n*Generated by BDH Benchmark Suite — benchmarks/run_all.py*")

    report = "\n".join(lines)
    with open(output_path, "w") as f:
        f.write(report)
    print(f"Report saved to {output_path}")
    return report


if __name__ == "__main__":
    results_file = sys.argv[1] if len(sys.argv) > 1 else "benchmarks/results.json"
    generate_report(results_file)
